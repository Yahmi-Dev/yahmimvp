<script lang="ts">
  export let report: any;
  export let userProfile: any;
  
  // Normalize report data: prefer nested AI payload if present, else use top-level fields
  const reportData = report?.report || report || {};
  
  // Core values with robust fallbacks
  $: score = (reportData?.score ?? report?.esgScore ?? 0) as number;
  $: summary = (reportData?.summary ?? '') as string;
  $: suggestions = (reportData?.suggestions ?? report?.recommendations ?? []) as string[];
  $: environmentalScore = (reportData?.environmentalScore ?? Math.floor(score * 0.4) ?? 0) as number;
  $: socialScore = (reportData?.socialScore ?? Math.floor(score * 0.3) ?? 0) as number;
  $: governanceScore = (reportData?.governanceScore ?? Math.floor(score * 0.3) ?? 0) as number;
  $: benchmarkPosition = (reportData?.benchmarkPosition ?? getBenchmarkFromScore(score)) as string;
  $: riskLevel = (reportData?.riskLevel ?? getRiskFromScore(score)) as string;
  $: quickWins = (reportData?.quickWins ?? []) as string[];
  $: longTermGoals = (reportData?.longTermGoals ?? []) as string[];
  $: complianceGaps = (reportData?.complianceGaps ?? []) as string[];
  
  function getBenchmarkFromScore(score: number): string {
    if (score >= 80) return "Leading";
    if (score >= 65) return "Above Average";
    if (score >= 45) return "Average";
    return "Below Average";
  }
  
  function getRiskFromScore(score: number): string {
    if (score >= 70) return "Low";
    if (score >= 50) return "Medium";
    return "High";
  }
  
  function getScoreColor(score: number): string {
    if (score >= 80) return "#2A9D8F"; // Green
    if (score >= 60) return "#E9C46A"; // Yellow
    if (score >= 40) return "#F4A261"; // Orange
    return "#E76F51"; // Red
  }
  
  function getRiskColor(risk: string): string {
    switch (risk) {
      case "Low": return "#2A9D8F";
      case "Medium": return "#E9C46A";
      case "High": return "#E76F51";
      default: return "#6C757D";
    }
  }
  
  function formatDate(timestamp: any): string {
    if (!timestamp) return "Unknown";
    const date = timestamp.seconds ? new Date(timestamp.seconds * 1000) : new Date(timestamp);
    return date.toLocaleDateString('en-US', { 
      year: 'numeric', 
      month: 'long', 
      day: 'numeric' 
    });
  }
  
  const downloadReport = () => {
    // Create a comprehensive report document
    const reportContent = `
YAHMI ESG SUSTAINABILITY REPORT
Generated: ${formatDate(report.generatedAt)}
Company: ${userProfile?.companyName || 'Your Organization'}
Industry: ${userProfile?.industry || 'Not specified'}

EXECUTIVE SUMMARY
Overall ESG Score: ${report.score}/100
Benchmark Position: ${benchmarkPosition}
Risk Level: ${riskLevel}

${report.summary}

DETAILED SCORES
Environmental: ${environmentalScore}/100
Social: ${socialScore}/100
Governance: ${governanceScore}/100

RECOMMENDATIONS
${report.suggestions?.map((s, i) => `${i + 1}. ${s}`).join('\n') || 'No specific recommendations available.'}

${report.quickWins ? `
QUICK WINS (High Impact, Low Effort)
${report.quickWins.map((w, i) => `${i + 1}. ${w}`).join('\n')}
` : ''}

${report.longTermGoals ? `
LONG-TERM STRATEGIC GOALS
${report.longTermGoals.map((g, i) => `${i + 1}. ${g}`).join('\n')}
` : ''}

${report.complianceGaps ? `
COMPLIANCE GAPS TO ADDRESS
${report.complianceGaps.map((g, i) => `${i + 1}. ${g}`).join('\n')}
` : ''}

This report was generated by Yahmi AI-powered sustainability assessment platform.
For more information, visit: https://yahmi.ai
    `.trim();
    
    const blob = new Blob([reportContent], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `Yahmi_ESG_Report_${formatDate(report.generatedAt).replace(/\s+/g, '_')}.txt`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };
</script>

